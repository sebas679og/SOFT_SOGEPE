package com.sagmade.model;

public class T_Areas {
	private int idArea;
	private String area;
	
	public T_Areas(int idArea, String area) {
		super();
		this.idArea = idArea;
		this.area = area;
	}

	public int getIdArea() {
		return idArea;
	}

	public void setIdArea(int idArea) {
		this.idArea = idArea;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}
	
		
}
